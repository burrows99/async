// Command gen writes promise/alln.go, the heterogeneous fixed-arity joins
// All2..All8.
//
// Go generics have no variadic type parameters, so a single All cannot accept
// promises of differing element types. Rather than hand-maintain seven nearly
// identical functions, this generator emits them. Regenerate with:
//
//	go generate ./...
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"strings"
)

// minN and maxN bound the generated arities. maxN is capped at 8: beyond a
// handful of results, callers should restructure rather than reach for All9.
const (
	minN = 2
	maxN = 8
)

func main() {
	var buf bytes.Buffer
	buf.WriteString(`// Code generated by internal/gen; DO NOT EDIT.

// This file provides All2..All8: heterogeneous, fixed-arity joins that All
// cannot express because Go generics have no variadic type parameters. Each is
// the typed-tuple analogue of All, with identical fail-fast semantics. Unlike
// All, these block and return their values directly, because a promise of a
// tuple cannot be destructured on Await.

package promise
`)
	for n := minN; n <= maxN; n++ {
		writeAllN(&buf, n)
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Fprintf(os.Stderr, "gen: gofmt failed: %v\n\n%s", err, buf.String())
		os.Exit(1)
	}
	if err := os.WriteFile("alln.go", src, 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "gen: write alln.go: %v\n", err)
		os.Exit(1)
	}
}

// writeAllN emits a single AllN function into buf. go/format tidies the spacing
// afterwards, so the raw output here favours simplicity over perfect alignment.
func writeAllN(buf *bytes.Buffer, n int) {
	types := make([]string, n)  // T1, T2, ...
	params := make([]string, n) // p1 *Promise[T1], ...
	values := make([]string, n) // v1, v2, ...
	zeros := make([]string, n)  // z1, z2, ...
	names := make([]string, n)  // p1, p2, ...
	for i := 1; i <= n; i++ {
		types[i-1] = fmt.Sprintf("T%d", i)
		params[i-1] = fmt.Sprintf("p%d *Promise[T%d]", i, i)
		values[i-1] = fmt.Sprintf("v%d", i)
		zeros[i-1] = fmt.Sprintf("z%d", i)
		names[i-1] = fmt.Sprintf("p%d", i)
	}

	fmt.Fprintf(buf, `
// All%[1]d waits for %[1]d promises of independent types to settle and returns
// their values as a flat tuple. It is the heterogeneous, fixed-arity analogue of
// [All]: join a known number of differently-typed promises without boxing them
// into interface{}.
//
// On the first failure, All%[1]d aborts the sibling tasks and returns zero
// values for every result together with that error, matching All's fail-fast
// semantics.
func All%[1]d[%[2]s any](%[3]s) (%[2]s, error) {
`, n, strings.Join(types, ", "), strings.Join(params, ", "))

	for i := 1; i <= n; i++ {
		fmt.Fprintf(buf, "\tvar v%d T%d\n", i, i)
	}
	fmt.Fprintf(buf, "\terrCh := make(chan error, %d)\n", n)
	fmt.Fprintf(buf, "\taborters := []aborter{%s}\n", strings.Join(names, ", "))
	for i := 1; i <= n; i++ {
		fmt.Fprintf(buf, "\tgo func() { var e error; v%d, e = p%d.Await(); errCh <- e }()\n", i, i)
	}
	fmt.Fprintf(buf, `	var firstErr error
	for i := 0; i < %d; i++ {
		if e := <-errCh; e != nil && firstErr == nil {
			firstErr = e
			for _, a := range aborters {
				a.Abort()
			}
		}
	}
	if firstErr != nil {
`, n)
	for i := 1; i <= n; i++ {
		fmt.Fprintf(buf, "\t\tvar z%d T%d\n", i, i)
	}
	fmt.Fprintf(buf, "\t\treturn %s, firstErr\n", strings.Join(zeros, ", "))
	fmt.Fprintf(buf, "\t}\n")
	fmt.Fprintf(buf, "\treturn %s, nil\n}\n", strings.Join(values, ", "))
}
